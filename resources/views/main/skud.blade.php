            отличие от электромеханических, окажутся в разблокированном состоянии. Это обусловлено подачей СКУД управляющего
            сигнала на разблокировку.
        </p>
        <h3 class="install__item-title">
            Принцип монтажа контроллеров
        </h3>
        <p>
            Одним из важных этапов в установке СКУД является установка контроллеров. Данные устройства предназначены для
            хранения в памяти ключей и событий, а также управления исполнительными устройствами. При установке контроллеров
            вся территория условно делится на наружную и внутреннюю. В случаях риска физического взлома или действий
            вандалов, контроллер необходимо разместить изнутри охраняемой территории.
        </p>
        <p>
            Так как электромагнитные и электромеханические замки управляются напряжением, следует ограничить доступ
            посторонних лиц к силовым проводам, что идут к замку. Особенно это важно, если провода подходят с наружной
            стороны. А в случае, если контроллеры используются на стратегических или режимных объектах, то доступ к проводам
            следует ограничить даже, если они подводятся с внутренней стороны помещения.
        </p>
        <p>
            Не стоит производить монтаж контроллеров системы СКУД своими силами, или силами неквалифицированных работников.
